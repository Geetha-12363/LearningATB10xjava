package literals;

public class ALLoperators {
    public static void main(String[] args) {
        int a=10;
        int b=20;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a<=b);
        System.out.println(a>=b);
        System.out.println(a==b);
        System.out.println(a!=b);
        a+=10;//a=a+10
        System.out.println(a);
        b-=10;//b=b-10
        System.out.println(b);
        a*=10;//a=a*10;
        System.out.println(a);
        a%=10;//a=a%10
        System.out.println(a);


    }
}
