package literals;

public class Comparison_Operator {
    public static void main(String[] args) {
        System.out.println(10>=10);
        System.out.println(10==10);
        System.out.println(10<=10);
        System.out.println(10>10);
        System.out.println(10<10);
        System.out.println(10==20);
    }
}
